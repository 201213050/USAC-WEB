public void comprobarPosiciones(int ancho, int alto)
{   
    int limite = scroll.getWidth(); 
    if(alto>yMax){yMax = alto;}    
    if((posX + ancho)>= limite-ancho)        
    {
        posX = 0;
        posY = posY + yMax;
        System.out.println("Salto de linea de :"+yMax);        
        yMax= 0;              
    }
    else
    {
        posX = posX + ancho;
    }

}

